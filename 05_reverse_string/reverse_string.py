def reverse_string(phrase):
    return phrase [::-1]
    # """Reverse string,
    #
    #     >>> reverse_string('awesome')
    #     'emosewa'
    #
    #     >>> reverse_string('sauce')
    #     'ecuas'
    # """
print (reverse_string('awesome'))
print (reverse_string('sauce'))
print (reverse_string('jenna'))